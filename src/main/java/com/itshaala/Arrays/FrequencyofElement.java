package com.itshaala.Arrays;

public class FrequencyofElement {
    public static void main(String[] args) {
        int [] array = {1, 2, 2, 3, 1, 4, 2};

        boolean[] visited = new boolean[array.length];

        for (int i = 0; i < array.length ; i++) {

            if (visited[i])
                continue;

        }
    }
}
